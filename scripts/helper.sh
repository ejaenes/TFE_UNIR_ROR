#!/bin/bash

set +u

# Paleta de colores
declare -r RESET='\033[0m'
declare -r GREEN='\033[38;5;2m'
declare -r RED='\033[38;5;1m'
declare -r YELLOW='\033[38;5;3m'

log() {
    printf "%b\n" "${*}" >&2
}

info() {
    log "${GREEN}INFO ${RESET} ==> ${*}"
}

warn() {
    log "${YELLOW}WARN ${RESET} ==> ${*}"
}

error() {
    log "${RED}ERROR ${RESET} ==> ${*}"
}

show_banner() {
  cat<<-'EOF'
------------------------------------------------------------------------
:::::::::   ::::::::  :::::::::           :::     :::::::::  :::::::::  
:+:    :+: :+:    :+: :+:    :+:        :+: :+:   :+:    :+: :+:    :+: 
+:+    +:+ +:+    +:+ +:+    +:+       +:+   +:+  +:+    +:+ +:+    +:+ 
+#++:++#:  +#+    +:+ +#++:++#:       +#++:++#++: +#++:++#+  +#++:++#+  
+#+    +#+ +#+    +#+ +#+    +#+      +#+     +#+ +#+        +#+        
#+#    #+# #+#    #+# #+#    #+#      #+#     #+# #+#        #+#        
###    ###  ########  ###    ###      ###     ### ###        ###        
------------------------------------------------------------------------
EOF
  info "Rails Environment: ${YELLOW}$RAILS_ENV${RESET}"
}
